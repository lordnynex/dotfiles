up () {
  COUNTER=$1
  while [[ $COUNTER -gt 0 ]]; do
    UP="${UP}../"
    COUNTER=$(( $COUNTER -1 ))
  done
  echo "cd $UP"
  cd $UP
  UP=''
}

bu() {
  cp $@ $@.backup-`date +%y%m%d`;
  echo "`date +%Y-%m-%d` backed up $PWD/$@" >> ~/.backups.log;
}

